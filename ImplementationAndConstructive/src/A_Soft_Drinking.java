    public static void solve()
    {
        Reader R = new Reader();

        

        int n = R.nextInt();
        int k = R.nextInt();
        int l = R.nextInt();
        int c = R.nextInt();
        int d = R.nextInt();
        int p = R.nextInt();
        int nl = R.nextInt();
        int np = R.nextInt();

        int t1 = (k * l) / nl;
        int t2 = c * d;
        int t3 = p / np;

        int min = Math.min(t1, t2);
        min = Math.min(min, t3);

        System.out.println(min/n);

    }
    
    static class Reader 
    { 
        BufferedReader br=new BufferedReader(new InputStreamReader(System.in));
        StringTokenizer st=new StringTokenizer("");
        public String next()
        {
            while(!st.hasMoreTokens())
            {
                try
                {
                    st=new StringTokenizer(br.readLine());
                }
                catch(Exception e)
                {
                    e.printStackTrace();
                }
            }
            return st.nextToken();
        }
        public int nextInt()
        {
            return Integer.parseInt(next());
        }
        public long nextLong()
        {
            return Long.parseLong(next());
        }
        public double nextDouble()
        {
            return Double.parseDouble(next());
        }
        public String nextLine()
        {
            try
            {
                return br.readLine();
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
            return null;
        }
        public boolean hasNext()
        {
            String next=null;
            try
            {
                next=br.readLine();
            }
            catch(Exception e)
            {
            }
            if(next==null)
            {
                return false;
            }
            st=new StringTokenizer(next);
            return true;
        }
    } 
